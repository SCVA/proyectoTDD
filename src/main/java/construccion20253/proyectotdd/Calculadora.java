class Calculadora {

    Integer suma(int sumandoUno, int sumandoDos) {
        return sumandoUno+sumandoDos;
    }

    Integer resta(int minuendo, int sustraendo) {
        return minuendo-sustraendo;
    }

    Integer multiplicar(int i, int i0) {
        return i*i0;
    }
    
}
